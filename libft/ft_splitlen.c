#include "libft.h"

int		ft_splitlen(char const *s, int i, char c)
{
	int		len;

	len = 0;
	while (s[i] == c && s[i])
		i++;
	while (s[i] != c && s[i])
	{
		i++;
		len++;
	}
	return (len);
}
