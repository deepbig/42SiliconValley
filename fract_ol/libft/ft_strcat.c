#include "libft.h"

char	*ft_strcat(char *restrict s1, const char *restrict s2)
{
	int		len;
	int		i;

	i = 0;
	while (s1[i] != '\0')
		i++;
	len = 0;
	while (s2[len] != '\0')
	{
		s1[i] = s2[len];
		i++;
		len++;
	}
	s1[i] = '\0';
	return (s1);
}
