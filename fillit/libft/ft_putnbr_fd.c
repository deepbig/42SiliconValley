#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char c;

	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		if (n == -2147483648)
		{
			ft_putchar_fd('2', fd);
			n = -147483648;
		}
		ft_putnbr_fd(-n, fd);
	}
	else if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		n = n % 10;
		c = (n + '0');
		ft_putchar_fd(c, fd);
	}
	else
	{
		ft_putchar_fd(n + '0', fd);
	}
}
