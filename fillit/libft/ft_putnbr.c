#include "libft.h"

void	ft_putnbr(int n)
{
	char c;

	if (n < 0)
	{
		ft_putchar('-');
		if (n == -2147483648)
		{
			ft_putchar('2');
			n = -147483648;
		}
		ft_putnbr(-n);
	}
	else if (n >= 10)
	{
		ft_putnbr(n / 10);
		n = n % 10;
		c = (n + '0');
		ft_putchar(c);
	}
	else
	{
		ft_putchar(n + '0');
	}
}
