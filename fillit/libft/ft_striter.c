#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int	i;
	int	len;

	i = 0;
	if (s == NULL)
		return ;
	len = ft_strlen(s);
	while (f && i < len)
	{
		f(&s[i]);
		i++;
	}
}
